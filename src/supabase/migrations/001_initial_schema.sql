-- Perfil automàtic després del registre.
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
begin
  insert into public.profiles (id, full_name, email)
  values (new.id, nullif(trim(new.raw_user_meta_data ->> 'full_name'), ''), new.email)
  on conflict (id) do nothing;
  return new;
end;
$$;

create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

-- Disponibilitat pública i anonimitzada: mai exposa user_id ni altres dades personals.
create or replace function public.get_court_availability(
  p_court_id bigint,
  p_date date
)
returns table (booking_date date, start_time time, end_time time)
language sql
stable
security definer
set search_path = public, pg_temp
as $$
  select b.booking_date, b.start_time, b.end_time
  from public.bookings b
  where b.court_id = p_court_id
    and b.booking_date = p_date
    and b.status = 'active'
  order by b.start_time;
$$;

-- Creació centralitzada: valida les regles i deixa la concurrència a PostgreSQL.
create or replace function public.create_booking(
  p_court_id bigint,
  p_booking_date date,
  p_start_time time,
  p_end_time time
)
returns public.bookings
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_user_id uuid := auth.uid();
  v_booking public.bookings;
begin
  if v_user_id is null then
    raise exception 'Has d’iniciar sessió.' using errcode = 'P0001';
  end if;

  if not exists (select 1 from public.courts where id = p_court_id) then
    raise exception 'La pista indicada no existeix.' using errcode = 'P0001';
  end if;

  if p_booking_date < current_date or p_booking_date > current_date + 60 then
    raise exception 'La data ha d’estar entre avui i els pròxims 60 dies.' using errcode = 'P0001';
  end if;

  if p_start_time < time '08:00' or p_end_time > time '22:00' or
     p_end_time - p_start_time <> interval '1 hour' or
     extract(minute from p_start_time) <> 0 or extract(second from p_start_time) <> 0 then
    raise exception 'La franja horària no és vàlida.' using errcode = 'P0001';
  end if;

  if p_booking_date + p_start_time <= (current_timestamp at time zone 'Europe/Madrid') then
    raise exception 'No es pot reservar una franja que ja ha començat.' using errcode = 'P0001';
  end if;

  insert into public.bookings (user_id, court_id, booking_date, start_time, end_time)
  values (v_user_id, p_court_id, p_booking_date, p_start_time, p_end_time)
  returning * into v_booking;

  return v_booking;
exception
  when exclusion_violation then
    raise exception 'La franja seleccionada se solapa amb una reserva existent.' using errcode = '23P01';
end;
$$;

-- Cancel·lació lògica: conserva l’historial i allibera la franja.
create or replace function public.cancel_booking(p_booking_id bigint)
returns public.bookings
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_user_id uuid := auth.uid();
  v_booking public.bookings;
begin
  if v_user_id is null then
    raise exception 'Has d’iniciar sessió.' using errcode = 'P0001';
  end if;

  select * into v_booking
  from public.bookings
  where id = p_booking_id and user_id = v_user_id;

  if not found then
    raise exception 'Reserva no trobada.' using errcode = 'P0001';
  end if;

  if v_booking.status = 'cancelled' then
    raise exception 'La reserva ja està cancel·lada.' using errcode = 'P0001';
  end if;

  if v_booking.booking_date + v_booking.start_time <= (current_timestamp at time zone 'Europe/Madrid') then
    raise exception 'No es pot cancel·lar una reserva que ja ha començat.' using errcode = 'P0001';
  end if;

  update public.bookings
  set status = 'cancelled', cancelled_at = now()
  where id = p_booking_id
  returning * into v_booking;

  return v_booking;
end;
$$;

alter table public.courts enable row level security;
alter table public.profiles enable row level security;
alter table public.bookings enable row level security;

create policy courts_select_public on public.courts
for select to anon, authenticated using (true);

create policy profiles_select_own on public.profiles
for select to authenticated using ((select auth.uid()) = id);

create policy profiles_update_own on public.profiles
for update to authenticated
using ((select auth.uid()) = id)
with check ((select auth.uid()) = id);

create policy bookings_select_own on public.bookings
for select to authenticated using ((select auth.uid()) = user_id);

