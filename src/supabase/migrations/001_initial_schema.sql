create extension if not exists btree_gist;

create table public.courts (
  id bigint generated by default as identity primary key,
  name text not null check (char_length(name) between 2 and 80),
  sport_type text not null check (sport_type in ('padel', 'tennis', 'futbol_sala', 'basquet', 'natacio', 'squash')),
  description text,
  capacity integer not null check (capacity > 0 and capacity <= 30),
  price_per_hour numeric(6, 2) not null check (price_per_hour >= 0),
  created_at timestamptz not null default now()
);

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text check (full_name is null or char_length(full_name) between 2 and 100),
  email text,
  created_at timestamptz not null default now()
);

create table public.bookings (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  court_id bigint not null references public.courts(id) on delete restrict,
  booking_date date not null,
  start_time time not null,
  end_time time not null,
  status text not null default 'active' check (status in ('active', 'cancelled')),
  cancelled_at timestamptz,
  created_at timestamptz not null default now(),
  constraint bookings_valid_time_range check (end_time > start_time),
  constraint bookings_one_hour check (end_time - start_time = interval '1 hour'),
  constraint bookings_opening_hours check (start_time >= time '08:00' and end_time <= time '22:00'),
  constraint bookings_full_hour check (
    extract(minute from start_time) = 0 and extract(second from start_time) = 0 and
    extract(minute from end_time) = 0 and extract(second from end_time) = 0
  ),
  constraint bookings_cancelled_consistency check (
    (status = 'active' and cancelled_at is null) or
    (status = 'cancelled' and cancelled_at is not null)
  )
);

-- Protecció atòmica contra dobles reserves, també davant peticions simultànies.
alter table public.bookings
  add constraint bookings_no_overlap
  exclude using gist (
    court_id with =,
    (tsrange(booking_date + start_time, booking_date + end_time, '[)')) with &&
  ) where (status = 'active');

create index bookings_user_date_idx on public.bookings (user_id, booking_date desc, start_time desc);
create index bookings_court_date_idx on public.bookings (court_id, booking_date, start_time)
  where status = 'active';
create index courts_sport_type_idx on public.courts (sport_type, name);

