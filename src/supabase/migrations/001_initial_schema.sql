grant select on public.profiles to authenticated;
grant update (full_name) on public.profiles to authenticated;
grant select on public.bookings to authenticated;

revoke execute on function public.get_court_availability(bigint, date) from public;
revoke execute on function public.create_booking(bigint, date, time, time) from public, anon;
revoke execute on function public.cancel_booking(bigint) from public, anon;
grant execute on function public.get_court_availability(bigint, date) to anon, authenticated;
grant execute on function public.create_booking(bigint, date, time, time) to authenticated;
grant execute on function public.cancel_booking(bigint) to authenticated;

revoke execute on function public.handle_new_user() from public, anon, authenticated;

commit;
